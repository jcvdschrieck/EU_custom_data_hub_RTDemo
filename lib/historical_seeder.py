def seed_historical_cases_db() -> int:
    init_historical_cases_db()
    rng = random.Random(_RNG_SEED)

    conn = _connect(HISTORICAL_CASES_DB)
    with conn:
        conn.execute("DELETE FROM Sales_Order_Case")
        conn.execute("DELETE FROM Sales_Order_Risk")
        conn.execute("DELETE FROM Sales_Order")
    conn.close()

    cases: list[dict] = []

    # High-similarity: live IE investigate clusters. Each cluster has
    # an explicit (n_retain, n_release) target so slide-1's three
    # recommendation branches are exercised across the live cases:
    # higher-risk clusters get retain-dominant histories, lower-risk
    # ones get release-dominant ones.
    for seller, cat, n_retain, n_release in _HIGH_SIM_TARGETS:
        zero_rate      = _CANON_RATE.get(cat, 0.23) == 0.0
        retain_pool    = _RETAIN_PROFILES_ZERO if zero_rate else _RETAIN_PROFILES_NONZERO
        for _ in range(n_retain):
            cases.append(_build_case(rng, seller, cat, rng.choice(retain_pool)))
        for _ in range(n_release):
            cases.append(_build_case(rng, seller, cat, rng.choice(_RELEASE_PROFILES)))

    # Medium: mixed profiles, same sellers but different categories.
    for seller, cat in _MEDIUM_SIM:
        profile = rng.choice(_PROFILES_MIXED)
        cases.append(_build_case(rng, seller, cat, profile))

    # Low: release-dominant profiles, unusual (seller, cat) combos.
    for seller, cat in _LOW_SIM:
        profile = rng.choice(_PROFILES_LOW)
        cases.append(_build_case(rng, seller, cat, profile))

    rng.shuffle(cases)

    conn = _connect(HISTORICAL_CASES_DB)
    with conn:
        for c in cases:
            _insert_case(conn, c)
    conn.close()

    from collections import Counter
    by_action  = Counter(c["case"]["Proposed_Action_Customs"] for c in cases)
    by_problem = Counter(c["case"]["VAT_Problem_Type"] for c in cases)
    by_cluster = Counter((c["orders"][0]["Seller_Name"], c["orders"][0]["HS_Product_Category"])
                         for c in cases)
    print(f"  {len(cases)} historical cases written")
    print(f"  outcomes: {dict(by_action)}")
    print(f"  problem types: {dict(by_problem)}")
    print(f"  top 5 (seller, cat) clusters:")
    for (s, cat), n in by_cluster.most_common(5):
        print(f"    {n}× {s[:24]:<24} / {cat}")

    return len(cases)


if __name__ == "__main__":
    n = seed_historical_cases_db()
    print(f"\n✓ {n} historical cases in {HISTORICAL_CASES_DB}")
