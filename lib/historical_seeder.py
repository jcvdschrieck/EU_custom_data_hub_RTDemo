def _draw_signals(rng: random.Random, profile: str) -> dict:
    """Produce a dict with the four engine signals + derived VAT_Misclass."""
    if profile == "high_vat_no_sr_confirm":
        vat_r = rng.uniform(0.50, 0.90)
        ml    = rng.uniform(0.40, 0.85)
        vague = rng.uniform(0.02, 0.20)
        sr    = 0.0
        gap_h = "above"
    elif profile == "high_vat_no_sr_nolimited":
        vat_r = rng.uniform(0.50, 0.90)
        ml    = rng.uniform(0.40, 0.75)
        vague = rng.uniform(0.02, 0.20)
        sr    = 0.0
        gap_h = "below"
    elif profile == "high_vat_with_sr":
        vat_r = rng.uniform(0.45, 0.85)
        ml    = rng.uniform(0.60, 0.95)
        vague = rng.uniform(0.05, 0.30)
        sr    = rng.uniform(0.70, 1.0)
        gap_h = rng.choice(["above", "below"])
    elif profile == "mid_vat_lowvague_confirm":
        vat_r = rng.uniform(0.35, 0.65)
        ml    = rng.uniform(0.30, 0.65)
        vague = rng.uniform(0.02, 0.40)
        sr    = 0.0
        gap_h = "above"
    elif profile == "mid_vat_lowvague_nolimited":
        vat_r = rng.uniform(0.30, 0.50)
        ml    = rng.uniform(0.25, 0.50)
        vague = rng.uniform(0.02, 0.40)
        sr    = 0.0
        gap_h = "below"
    elif profile == "low_vague_no_sr":
        vat_r = rng.uniform(0.00, 0.20)
        ml    = rng.uniform(0.00, 0.25)
        vague = rng.uniform(0.55, 0.85)
        sr    = 0.0
        gap_h = "below"
    elif profile == "low_clean_no_sr":
        vat_r = rng.uniform(0.00, 0.15)
        ml    = rng.uniform(0.00, 0.15)
        vague = rng.uniform(0.02, 0.30)
        sr    = 0.0
        gap_h = "below"
    else:
        raise ValueError(f"Unknown profile: {profile}")

    vat_misclass = max(vat_r, ml) * 100.0   # slide-2 combo rule
    return {
        "engine_vat_ratio":   round(vat_r, 3),
        "engine_ml":          round(ml, 3),
        "engine_vague":       round(vague, 3),
        "engine_ie_seller":   round(sr, 3),
        "vat_misclass":       round(vat_misclass, 1),
        "gap_hint":           gap_h,
    }


_VALUE_CAP = 149.99   # stay strictly below the €150 IOSS threshold
_CLUSTER_LO = 0.894   # ±11% window → max/min ≤ 1.25 between any two orders
_CLUSTER_HI = 1.118


def _cluster_base_for_gap_hint(rng: random.Random, hint: str, applied: float,
                               expected: float, n_orders: int) -> float:
    """Pick a *case-level* base product value so the per-order values —
    each drawn within ±11% of this base — produce a total VAT gap that
    matches the hint ("above" > 1€, "below" ≤ 1€). A shared base keeps
    every order inside the case within 25% of every other order, which
    is how real invoices cluster.
    """
    if applied >= expected:
        return rng.uniform(20.0, _VALUE_CAP / _CLUSTER_HI)
    delta = expected - applied
    if hint == "above":
        # Per-order gap needs headroom so n_orders × base × delta > ~1.5 €.
        floor = max(30.0, 1.5 / (n_orders * delta) + 5.0)
        return rng.uniform(floor, _VALUE_CAP / _CLUSTER_HI)
    # "below": n_orders × base × delta ≤ ~0.9 €
    ceil = max(1.5, 0.9 / (n_orders * delta))
    return rng.uniform(1.0, ceil)


def _draw_cluster_value(rng: random.Random, base: float) -> float:
    """Per-order value within ±11% of a case's cluster base, rounded to
    2dp and capped strictly below €150."""
    v = rng.uniform(base * _CLUSTER_LO, base * _CLUSTER_HI)
    return round(min(v, _VALUE_CAP), 2)


