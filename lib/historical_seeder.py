"""
Historical-case seeder — signal-driven, aligned with Rules in App.pptx slide 2.

Populates data/historical_cases.db with ~35 past closed IE cases
spanning Sep 2025 – Mar 2026 so the C&T frontend's Previous Cases panel
has realistic reference data independent of the live simulation in
investigation.db.

Each historical case is generated by:

  1. Picking a (seller, parent_category) cluster that either matches a
     live IE investigate cluster (high-similarity), a same-seller other
     category (medium), or a neutral seller/category mix (low).

  2. Drawing the four engine signals for the case:
       VAT_Misclass = max(Engine_VAT_Ratio, Engine_ML_Watchlist) * 100
         (slide-2 "VAT Misclass" treated as a combo of the VAT test and
          the ML test — the higher of the two drives the row selection)
       VAT_Vague    = Engine_Description_Vagueness * 100
       S.R (bool)   = Engine_IE_Seller_Watchlist > 0
       $$           = VAT_Gap_Fee

  3. Applying the slide-2 decision cascade (see _decide_from_signals)
     to derive Proposed_Action_Customs + Proposed_Action_Tax + closure.

  4. Emitting 1–4 linked sales orders + 1 risk row + 1 case row.

Thresholds follow slide 2 (hard 30/70 cuts on VAT Misclass, 50 on
VAT Vague, 1 € on $$). We have some freedom on the combo formula — we
picked max() over average so a single strong signal still flags
mis-classification, matching how the table reads when VAT Misclass is
the dominant signal.

Run via seed_databases.py or directly:
    python -m lib.historical_seeder
"""
