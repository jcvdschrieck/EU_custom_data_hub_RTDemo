def _decide_from_signals(vat_misclass: float, vague: float,
                         sr: bool, vat_gap: float) -> tuple[str, str, str]:
    """Apply slide 2's decision cascade. Returns
    (problem_type, proposed_action_customs, proposed_action_tax).

    Problem type mirrors the case-type column on slide 2:
      "VAT Misclassification" when VAT Misclass ≥ 30
      "VAT Vague" otherwise
    Cases that land on "Request 3rd Party" are resolved post-factum
    (see _resolve_3rd_party) since historical cases must be closed.
    """
    if vat_misclass > 70:
        # Row 1 / Row 2
        problem = "VAT Misclassification"
        customs = "tax_review"
        if sr:
            tax = "risk_confirmed"                     # row 2
        else:
            tax = "risk_confirmed" if vat_gap > 1.0 else "no_limited_risk"  # row 1
        return problem, customs, tax

    if 30 <= vat_misclass <= 70:
        # Rows 3, 4, 5
        problem = "VAT Misclassification"
        if vague > 50 and not sr:
            return problem, "input_requested", ""     # row 3
        if vague > 50 and sr:
            return problem, "tax_review", "input_requested"  # row 4 — AI uncertain → 3rd party
        # row 5: low vagueness → Tax decides on $$
        tax = "risk_confirmed" if vat_gap > 1.0 else "no_limited_risk"
        return problem, "tax_review", tax

    # VAT Misclass < 30
    if vague > 50:
        problem = "VAT Vague"
        if sr:
            return problem, "tax_review", "input_requested"  # row 6 — AI uncertain
        return problem, "release", ""                         # row 7
    # No signals firing at all → auto-release path (not in table explicitly
    # but follows from the Risk Engine >80 / <30 / 30-80 guide).
    return "No Risk", "release", ""


def _resolve_3rd_party(rng: random.Random, problem: str,
                       vat_misclass: float, vague: float, sr: bool,
                       vat_gap: float) -> tuple[str, str]:
    """Historical cases are closed, so a Request 3rd Party branch must
    be followed through to its eventual resolution. We model the 3rd
    party coming back with evidence: seller-risk cases almost always
    close retain; clean cases tilt release."""
    if sr:
        return ("retainment", "risk_confirmed")
    if vat_misclass >= 30 and vat_gap > 1.0:
        return ("retainment", "risk_confirmed")
    return ("release", "no_limited_risk")


