# ── Helpers ─────────────────────────────────────────────────────────────────

def expected_rate_for(destination: str, subcategory_code: str) -> float | None:
    """Return the canonical VAT rate (as fraction, e.g. 0.23) for this
    (destination, subcategory) pair. Falls back to the country standard
    rate if the exact pair is not in VAT_RATE_LOOKUP. Returns None only
    if the destination itself is unknown."""
    pair = VAT_RATE_LOOKUP.get((destination, subcategory_code))
    if pair is not None:
        return pair[0]
    return COUNTRY_STANDARD_RATE.get(destination)


def expected_rate_type_for(destination: str, subcategory_code: str) -> str | None:
    """Return the VAT rate-type code ({S, R1, SR, Z}) for this pair, or
    None on miss / unknown destination. Mirrors expected_rate_for shape."""
    pair = VAT_RATE_LOOKUP.get((destination, subcategory_code))
    return pair[1] if pair is not None else None


def parent_category_for(subcategory_code: str) -> str | None:
    """Look up the parent VAT category for a subcategory code."""
    entry = SUBCATEGORY_BY_CODE.get(subcategory_code)
    return entry[0] if entry else None


